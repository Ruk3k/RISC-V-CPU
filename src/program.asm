// --- I型命令: 基本演算 ---
addi  x1,  x0,  10      // x1 = 10
addi  x2,  x0,  20      // x2 = 20
addi  x3,  x0,  -5      // x3 = -5 (負数)
addi  x4,  x0,  0       // x4 = 0 (ゼロ)

// --- I型命令: 境界値テスト ---
addi  x5,  x0,  2047    // x5 = 2047 (12bit最大正数)
addi  x6,  x0,  -2048   // x6 = -2048 (12bit最小負数)
addi  x7,  x1,  -10     // x7 = 10 + (-10) = 0 (相殺)

// --- I型命令: ビット演算 ---
andi  x8,  x1,  15      // x8 = 10 & 15 = 10
andi  x9,  x2,  7       // x9 = 20 & 7 = 4
ori   x10, x1,  5       // x10 = 10 | 5 = 15
ori   x11, x3,  -1      // x11 = -5 | -1 = -1 (全ビット1)
xori  x12, x1,  3       // x12 = 10 ^ 3 = 9
xori  x13, x2,  -1      // x13 = 20 ^ -1 = -21 (ビット反転)

// --- R型命令: 基本演算 ---
add   x14, x1,  x2      // x14 = 10 + 20 = 30
add   x15, x3,  x4      // x15 = -5 + 0 = -5
sub   x16, x2,  x1      // x16 = 20 - 10 = 10
sub   x17, x1,  x2      // x17 = 10 - 20 = -10

// --- R型命令: ゼロとの演算 ---
add   x18, x4,  x1      // x18 = 0 + 10 = 10
sub   x19, x1,  x4      // x19 = 10 - 0 = 10
and   x20, x1,  x4      // x20 = 10 & 0 = 0
or    x21, x1,  x4      // x21 = 10 | 0 = 10
xor   x22, x1,  x4      // x22 = 10 ^ 0 = 10

// --- R型命令: ビット演算 ---
and   x23, x1,  x2      // x23 = 10 & 20 = 0
or    x24, x1,  x2      // x24 = 10 | 20 = 30
xor   x25, x1,  x2      // x25 = 10 ^ 20 = 30
and   x26, x5,  x6      // x26 = 2047 & -2048 = 0
or    x27, x5,  x6      // x27 = 2047 | -2048 = -1

// --- 特殊ケース: x0レジスタ ---
addi  x0,  x1,  100     // x0は常に0（書き込み無効）
add   x28, x0,  x1      // x28 = 0 + 10 = 10 (x0は常に0)
